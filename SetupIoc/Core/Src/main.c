double calculate_Temp(uint8_t* buffer)
{
	uint8_t sign = *buffer >> 7;
	uint8_t whole = 0;
	uint8_t decimal = 0;
	double temp = 0.0;

	if(*(buffer + 1) != 0)
		decimal = 5;

	whole = (*buffer) & 0x7F;

	if(sign == 1) {
		printf("Trenutna: -%u.%u C ", (uint8_t)whole, (uint8_t)decimal);
	} else {
		printf("Trenutna: %u.%u°C ", (uint8_t)whole, (uint8_t)decimal);
	}

	if(*(buffer + 1) != 0)
		temp = 0.5;

	temp = temp + whole;
	if(sign == 1)
		temp = -temp;

	return temp;
}

int point(double temp) {
	if((int)temp != ceil(temp))
		return 5;
	return 0;
}

void init_EXTI4() {
	NVIC_SetPriority(EXTI4_IRQn, 6);
	NVIC_EnableIRQ(EXTI4_IRQn);

	SYSCFG->EXTICR[1] |= 0x4;
	EXTI->FTSR |= 1 << 4;
	EXTI->IMR |= 1UL << 4;
}

void serveEncoder(void)
{
	if(bState == 1)
		globalPosition++;
	else
		globalPosition--;

	if(MID_TEMP + 0.5 * globalPosition < MIN_TEMP) {
		globalPosition++;
		return;
	}

	if(MID_TEMP + 0.5 * globalPosition > MAX_TEMP) {
		globalPosition--;
		return;
	}

	desiredTemp = MID_TEMP + 0.5 * globalPosition;
}

void __attribute__((interrupt)) EXTI4_IRQHandler(void) {
	bState = (GPIOE->IDR >> 5) & 0x1;

	serveEncoder();

	printf("Interrupt number: %u \r\n", numOfRequests++);
	EXTI->PR |= (1 << 4);
}

